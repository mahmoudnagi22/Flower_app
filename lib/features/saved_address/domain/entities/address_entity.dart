class AddressEntity {
  final int id;
  final String city;
  final String street;
  final String phone;
  final String userName;
  final String lat;
  final String long;

  AddressEntity({
    required this.id,
    required this.city,
    required this.street,
    required this.phone,
    required this.userName,
    required this.lat,
    required this.long,
  });
}
