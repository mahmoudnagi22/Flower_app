class AddressEntity {
  final int id;
  final String city;
  final String phoneNumber;
  final String recipientName;
  final String address;
  final String area;

  AddressEntity({
    required this.id,
    required this.city,
    required this.phoneNumber,
    required this.recipientName,
    required this.address,
    required this.area,
  });
}
